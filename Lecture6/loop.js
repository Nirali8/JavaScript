/*While Loop */
{
  let age = 1;
  while (age < 18) {
    console.log("You are Minor!");
    age++;
  }
}
/*do While Loop */
{
  let age = 1;
  do {
    console.log(`Age is ${age}`);
  } while (age == 2);
}
/*for loop */
{
  let num1 = 19;
  for (let i = 1; i <= 10; i++) {
    console.log(`${num1} * ${i} = ${num1 * i}`);
  }
}
