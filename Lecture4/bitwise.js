/*  Bitwise Operator */
/* bitwise AND()
when bitwise is AND is performed on pair of bits it returns 1 if both bits are 1
*/
console.log("AND");
{
  let x = 1;
  let y = 0;
  console.log(x & y);
  console.log(y & x);
  console.log(x & x);
  console.log(y & y);
}

/* bitwise OR()
when bitwise is OR is performed on pair of bits it returns 1 if one of the bits is 1
*/
console.log("OR");
{
  let x = 1;
  let y = 0;
  console.log(x | y);
  console.log(y | x);
  console.log(x | x);
  console.log(y | y);
}
/* bitwise XOR()
when bitwise is XOR is performed on pair of bits it returns 1 if  bits are different
*/
console.log("XOR");
{
  let x = 1;
  let y = 0;
  console.log(x ^ y);
  console.log(y ^ x);
  console.log(x ^ x);
  console.log(y ^ y);
}
